      <h1 class="principalh1">CONOCENOS</h1> 
      <img src="./img/logo.png" class="logogrande">  
      <p>Es en Estados Unidos, en donde se creo el primer mercado de autoservicio. Creado por  Clarence Saunders el 16 de septiembre de 1916,  a quien se le ocurrió hacer una tienda en la que el cliente se sirviera todo lo que necesitaba, pasando a pagarlo posteriormente ante un empleado, que se encargaba de revisarlo y cobrarlo saliendo el cliente plenamente satisfecho.</p>
      <p>Es así que nació el autoservicio, pero en realidad dicen que no fue con la finalidad de hacer la vida mas fácil y rápida al cliente como sucede hoy en día, sino que es de esta forma como redujo el número de dependientes que atendían su tienda ahorrando tiempo y dinero en la venta de productos, productos que otros empleados solo debían reponer en los estantes del establecimiento.</p>
      <p>Nacían Piggly Wiggly (cerdito ondulado), la primera cadena de supermercados del mundo, nadie supo el porque eligió este nombre tan sui géneris para su cadena de tiendas, pero hay quienes cuentan, que se le ocurrió cuando vio a unos cerditos intentar pasar por debajo de una valla.</p>
      <p>Es el 21 de Octubre del mismo año (1916), que patentó el supermercado asegurándose así, el que los clientes puedan ver todos los productos en su comercio antes de salir, evitando enseñarles los productos uno a uno en el mostrador, perdiendo así tiempo y dinero.</p>

